x = 5
y = 10

#karena x lebih kecil dari y adalah benar, maka kode didalam scope statemen id akan dijalankan.
if (x < y):
    print('Nilai x lebih kecil dari y') #ini akan dijalankan
#karena x lebih besar dari y adalah salah, maka kode di dalam scope statemen ini tidak dijalankan.
if (x > y):
    print('Nilai x lebih besar dari y') #ini tidak akan dijalankan